//if..else statment
let num = 0

if (num > 0)
{
    print(" \(num) is a positive number")
}

else if (num < 0 )
{
    print("\(num) is negative number")
}

else {
    print("number is zero -> \(num)")
}


//switch case statments
let dayOfWeek = 0

switch dayOfWeek{
case 1:
    print("sunday")
case 2:
    print("monday")
case 3:
    print("tuesday")
case 4:
    print("wednesday")
case 5:
    print("thursday")
case 6:
    print("friday")
case 7:
    print("saturday")
default:
    print("Invalid")
}
